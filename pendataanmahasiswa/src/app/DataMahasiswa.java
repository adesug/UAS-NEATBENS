    private void loadData (String key){
         try {
             model.setRowCount(0);
             String where = !key.isEmpty() ? "WHERE nama LIKE '%"+key+"%' OR nim LIKE '%"+key+"%' OR prodi LIKE '%"+key+"%' OR alamat LIKE '%"+key+"%' OR telp LIKE '%"+key+"%'  " : "";
             String q = "SELECT * FROM mahasiswa "+where;
             Connection c = Koneksi.MySQL();
             Statement st = c.createStatement();
             ResultSet rs = st.executeQuery(q);
             int nomor = 0;
             while (rs.next()) {
                 int id = rs.getInt("id_mahasiswa");
                 String nama = rs.getString("nama");
                 String nim = rs.getString("nim");
                 String prodi = rs.getString("prodi");
                 String alamat = rs.getString("alamat");
                 String telp = rs.getString("telp");
                         nomor++;
                         Object[] data = {id,nomor, nama, nim, prodi, alamat, telp};
                         model.addRow(data);    
            }
         } catch (Exception e) {
         }
     }
     
